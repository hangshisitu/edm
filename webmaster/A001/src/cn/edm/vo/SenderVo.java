package cn.edm.vo;

public class SenderVo {
	
	private String sender;
	private Integer sumSentCount;
	private Integer sumReachCount;
	private Integer sumForwardCount;
	private Integer sumClickCount;
	private Integer sumReadCount;
	private String taskIdItems;
	
	public String getSender() {
		return sender;
	}
	public void setSender(String sender) {
		this.sender = sender;
	}
	public Integer getSumSentCount() {
		return sumSentCount;
	}
	public void setSumSentCount(Integer sumSentCount) {
		this.sumSentCount = sumSentCount;
	}
	public Integer getSumReachCount() {
		return sumReachCount;
	}
	public void setSumReachCount(Integer sumReachCount) {
		this.sumReachCount = sumReachCount;
	}
	public Integer getSumForwardCount() {
		return sumForwardCount;
	}
	public void setSumForwardCount(Integer sumForwardCount) {
		this.sumForwardCount = sumForwardCount;
	}
	public Integer getSumClickCount() {
		return sumClickCount;
	}
	public void setSumClickCount(Integer sumClickCount) {
		this.sumClickCount = sumClickCount;
	}
	public Integer getSumReadCount() {
		return sumReadCount;
	}
	public void setSumReadCount(Integer sumReadCount) {
		this.sumReadCount = sumReadCount;
	}
	public String getTaskIdItems() {
		return taskIdItems;
	}
	public void setTaskIdItems(String taskIdItems) {
		this.taskIdItems = taskIdItems;
	}
	
	

}
